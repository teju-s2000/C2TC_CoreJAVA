package com.tns.equalsoperator;

public class DataTypes {

	  public static void main(String[] args) {
	        Long l1 = 155l;
	        Long l2 = 155l; 
	        
	        Double d1 = 15.5d;
	        Double d2 = 20.0d;
	        
	        Boolean b1 = true;
	        Boolean b3 = false;
	         
	         
	        System.out.println("l1 and l2 are equal : "+(l1.equals(l2)));  
	        System.out.println("d1 and d2 are equal : "+(d1.equals(d2)));
	        System.out.println("b1 and b3 are equal : "+(b1.equals(b3)));
	  }      
}
